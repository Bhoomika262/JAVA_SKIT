class DataTypes{
    public static void main(String[] args)
    {
        char c='A';
        float f=3.14f;
        double d=3.1232214532;
        String s="Bhoomi";
        System.out.println("Character: "+c);
        System.out.println("Float: "+f);
        System.out.println("Double: "+d);
        System.out.println("String: "+s);
    }
}